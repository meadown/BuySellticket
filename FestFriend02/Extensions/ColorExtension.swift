import UIKit

extension UIColor {
    
    static var appGreenColor: UIColor {
        get {
            return UIColor(red: 118/255, green: 214/255, blue: 114/255, alpha: 1)
        }
    }
    
    static var appRedColor: UIColor {
        get {
            return UIColor(red: 236/255, green: 102/255, blue: 99/255, alpha: 1)
        }
    }
    
    static var appBlueColor: UIColor {
        get {
            return UIColor(red: 0/255, green: 156/255, blue: 222/255, alpha: 1)
        }
    }
}
